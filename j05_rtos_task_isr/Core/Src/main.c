/**
* @brief Function implementing the TaskA thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTaskA */
void StartTaskA(void *argument)
{
  /* USER CODE BEGIN StartTaskA */
  uint16_t received_number;

  /* Infinite loop */
  for(;;)
  {
    // 等待從佇列中接收數據，無限期等待
    if (osMessageQueueGet(NumberQueueHandle, &received_number, NULL, osWaitForever) == osOK)
    {
      // 根據接收到的數字執行不同的動作
      if (received_number == 0)
      {
        printf("happy new year ~\r\n");
      }
      else
      {
        printf("Countdown: %u\r\n", received_number);
      }
    }
  }
  /* USER CODE END StartTaskA */
}

/* USER CODE BEGIN Header_StartTaskB */
/**
* @brief Function implementing the TaskB thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTaskB */
void StartTaskB(void *argument)
{
  /* USER CODE BEGIN StartTaskB */
  // 初始化計數器為起始數字
  uint16_t current_number = COUNTDOWN_START;

  /* Infinite loop */
  for(;;)
  {
    // 檢查是否有來自按鈕中斷的重置請求
    if (g_reset_request == 1)
    {
      // 重置計數器
      current_number = COUNTDOWN_START;
      // 清除重置標誌
      g_reset_request = 0;
      printf("---- Counter Reset ----\r\n");
    }

    // 將當前數字放入佇列。timeout 設為 0 表示如果佇列已滿則不等待。
    osMessageQueuePut(NumberQueueHandle, &current_number, 0U, 0U);

    // 如果數字大於 0，則遞減。如果等於 0，則保持為 0。
    if (current_number > 0)
    {
      current_number--;
    }

    // 延遲 1000 毫秒 (1 秒)
    osDelay(1000);
  }
  /* USER CODE END StartTaskB */
}

